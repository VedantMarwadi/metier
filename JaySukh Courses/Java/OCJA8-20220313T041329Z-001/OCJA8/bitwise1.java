class bitwise1
{
	public static void main(String arg[])
	{
		int a=10;
		int b=5;
		int c=a & b;
		System.out.println(c);
		c=a | b;
		System.out.println(c);
		c=a ^ b;
		System.out.println(c);
		c=a<<3;
		System.out.println(c);
		c=a>>2;
		System.out.println(c);
		c=~a;
		System.out.println(c);

		//System.out.println(f1.exists() ? "File Exists":"File does not Exists");



	}
}